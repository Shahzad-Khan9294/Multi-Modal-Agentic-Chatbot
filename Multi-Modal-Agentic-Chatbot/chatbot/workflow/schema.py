class DateRange(BaseModel):
    start_date: Optional[datetime] = Field(
        description="""
        Explicit or relative start date for a date range mentioned in the user question.
        Examples:
        - Single calendar dates: "January 6", "March 3", "from March 3 to April 16"
        - If the user mentions "today" or "today’s", set the start_date to today’s date.
        Note: Only fill this for actual date ranges or relative expressions that indicate a start date.
        Do NOT fill with numeric counts like 'last 10 days' here.
        """,
        default=None
    )
    end_date: Optional[datetime] = Field(
        description="""
        Explicit or relative end date for a date range mentioned in the user question.
        Examples:
        - Single calendar dates: "April 16"
        - If the user mentions "today" or "today’s", set the end_date to today’s date.
        Note: Only fill this for actual date ranges or relative expressions that indicate an end date.
        Do NOT fill with numeric counts like 'last 10 days' here.
        """,
        default=None
    )
    calendar_month: Optional[Union[str, List[str]]] = Field(
        description="""
        Month names explicitly mentioned in the user question.
        Examples: "January", "March", "December"
        Can be used with date ranges or counts, e.g., "last 20 days of January"
        """,
        default=None
    )
    days: Optional[int] = Field(
        description="""
        Number of day(s) mentioned as a duration or count in the user question.
        Examples: "last 10 days", "previous 5 days", "last 1 day", "past 20 days"
        Note: Use this for numeric counts, not absolute dates.
        
        - If user question mentions "recent" or "latest" set only `days` as 1.
        - Also set `days` as 1 when a day is mentioned without a number like: "yesterday/previous/past/last day(s)".
        - For phrases that are mentioning like: "last few days" or "past/previous few day(s)", set `days` as 5.
        """,
        default=0
    )
    weeks: Optional[int] = Field(
        description="""
        Number of week(s) mentioned as a duration or count in the user question.
        Examples: "last 2 weeks", "previous 4 weeks",  "last 1 week"
        Note: Use this for numeric counts, not absolute dates.

        - **Look for unnumbered durations in user question**: Set `weeks` as 1 when a week is mentioned like: "previous/past/last week(s)".
        - For phrases that are mentioning like: "last **few** weeks" or "past/previous few week(s)", set `weeks` as 2.
        """,
        default=0
    )
    months: Optional[int] = Field(
        description="""
        Number of month(s) mentioned as a duration or count in the user question.
        Examples: "last 3 months", "previous 1 month"
        Note: Use this for numeric counts, not absolute dates.

        - **Look for unnumbered durations in user question**: Set `months` as 1 when a month is mentioned like: "previous/past/last month(s)".
        - For phrases that are mentioning like: "last few months" or "past/previous **few** month(s)", set `months` as 3.
        """,
        default=0
    )
    years: Optional[int] = Field(
        description="""
        Number of year(s) or specific year mentioned in the user question.
        Examples: "last 2 years", "previous year", "2024", "last year"
        If no specific year is defined, set to 0.
        Note: Use this for numeric counts or explicitly mentioned years.
        
        - **Look for unnumbered durations in user question**: Set `years` as 1 when a year is mentioned like: "previous/past/last year".
        """,
        default=0
    )


